import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { hover?: boolean; glow?: boolean }
>(({ className, hover, glow, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border border-[#1F2937] bg-[#111827] text-[#F8FAFC] shadow-sm",
      hover && "transition-all duration-200 hover:border-[#7C3AED]/40 hover:shadow-lg hover:shadow-purple-900/10 hover:-translate-y-0.5 cursor-pointer",
      glow && "animate-pulse-glow",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

